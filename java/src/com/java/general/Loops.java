package com.java.general;

public class Loops {
	public static void main(String[] args) {

		// While Loop
		int x = 0;
		
		while (x < 10) {
			System.out.println(x);
			x++;
		}
		
		// Do-While Loop
		do {
			System.out.println(x);
			x++;
		} while (x < 10);
		
		// For Loop
		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
	}
}
